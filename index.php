    <script>
        const rootElement = document.querySelector(":root");
        const audioIconWrapper = document.querySelector(".audio-icon");
        const audioIcon = document.querySelector('.audio-icon i');
        const song = document.querySelector('#song');
        let isPlaying = false;


        function disableScroll() {
            const scrolltop = window.pageYOffset || document.documentElement.scrolltop;
            const scrollleft = window.pageXOffset || document.documentElement.scrollleft;

            window.onscroll = function() {
                window.scrollTo(scrolltop, scrollleft);
            }


            rootElement.style.scrollBehavior = 'auto';
        }

        function enableScroll() {
            window.onscroll = function() {}
            rootElement.style.scrollBehavior = 'smooth';
            // localStorage.setItem('opened', 'true')
            playAudio();
        }

        function playAudio() {

            // song.volume = 0.5;
            audioIconWrapper.style.display = 'flex';
            song.play();
            isPlaying = true;
        }

        audioIconWrapper.onclick = function() {
            if (isPlaying) {
                song.pause();
                audioIcon.classList.remove('bi-pause-circle-fill');
                audioIcon.classList.add('bi-play-circle-fill');
            } else {
                song.play();
                audioIcon.classList.add('bi-pause-circle-fill');
                audioIcon.classList.remove('bi-play-circle-fill');
            }
            isPlaying = !isPlaying;
        }


        // if (!localStorage.getItem('opened')) {
        //     disableScroll();
        // }

        disableScroll();
    </script>

